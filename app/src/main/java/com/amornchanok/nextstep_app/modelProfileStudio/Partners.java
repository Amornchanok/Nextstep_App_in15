package com.amornchanok.nextstep_app.modelProfileStudio;

public class Partners {
    private String firstname;
    private String lastname;
    private String birthday;
    private String gender;
    private String email;
    private String password;
    private String phone;
    private boolean otpstatus;
    private String imgPartner;
    private String imgIdCard;
    private String studioId;

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public boolean isOtpstatus() {
        return otpstatus;
    }

    public void setOtpstatus(boolean otpstatus) {
        this.otpstatus = otpstatus;
    }

    public String getImgPartner() {
        return imgPartner;
    }

    public void setImgPartner(String imgPartner) {
        this.imgPartner = imgPartner;
    }

    public String getImgIdCard() {
        return imgIdCard;
    }

    public void setImgIdCard(String imgIdCard) {
        this.imgIdCard = imgIdCard;
    }

    public String getStudioId() {
        return studioId;
    }

    public void setStudioId(String studioId) {
        this.studioId = studioId;
    }
}
